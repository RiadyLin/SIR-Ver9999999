    private void refreshTable() {
        garage = AppUtil.getService().getGarages();
        tbGarage.setModel(new AccessTableModel(garage));
        tbGarage.getColumnModel().getColumn(0).setMinWidth(0);
        tbGarage.getColumnModel().getColumn(0).setMaxWidth(0);
    }

    private class AccessTableModel extends AbstractTableModel {

        private final List<Garage> listGarages;
        private final String[] tableHeaders = {"Record Id", "Name", "Description"};

        public AccessTableModel(List<Garage> listGarages) {
            this.listGarages = listGarages;
        }

        @Override
        public int getRowCount() {
            return listGarages.size();
        }

        @Override
        public int getColumnCount() {
            return 3;
        }

        @Override
        public String getColumnName(int columnIndex) {
            return tableHeaders[columnIndex];
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Garage p = garage.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return p.getRecordId();
                case 1:
                    return p.getName();
                case 2:
                    return p.getDescription();
                default:
                    return "";
            }
        }
    }
    
