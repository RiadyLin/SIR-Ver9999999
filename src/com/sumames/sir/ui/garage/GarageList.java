    private void tfSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchKeyReleased
        for (int i = 0; i < tbGarage.getRowCount(); i++) {
            if (tbGarage.getValueAt(i, 1).toString().startsWith(tfSearch.getText())) {
                //select baris yang ditemukan
                tbGarage.getSelectionModel().setSelectionInterval(i, i);
                //scroll ke baris tersebut kalau ada di bawah atau bagian atas 
                ComponentUtils.scrollToRect(tbGarage, i);
                break;
            } else if (tbGarage.getValueAt(i, 2).toString().startsWith(tfSearch.getText())) {
                //select baris yang ditemukan
                tbGarage.getSelectionModel().setSelectionInterval(i, i);
                //scroll ke baris tersebut kalau ada di bawah atau bagian atas 
                ComponentUtils.scrollToRect(tbGarage, i);
                break;
            }
        }
    }//GEN-LAST:event_tfSearchKeyReleased

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        if (tbGarage.getSelectedRow() >= 0) {
            Garage garage = AppUtil.getService().getGarageById(Integer.valueOf(tbGarage.getValueAt(tbGarage.getSelectedRow(), 0).toString()));
            if (garage != null) {
                garage.setDeleteDatetime(new Date());
                garage.setDeletebyuserRecordid(Main.getFrame().getLogin().getEmployeeRecordId());
                if (AppUtil.getService().save(garage)) {
                    msg("Delete Done!");
                    refreshTable();
                } else {
                    msg("Delete Failed!");
                }

            }

        }
    }//GEN-LAST:event_btDeleteActionPerformed


